print('Modelos de operadores')

numberOne = 20
numberTwo = 10

# Plus
print(numberOne + numberTwo)

# Minus
print(numberOne - numberTwo)

# Float Division
print(numberOne / numberTwo)

# Integer Division
print(numberOne // numberTwo)

# Modulus
print(numberOne % numberTwo)

# Expo
print(numberOne ** numberTwo)